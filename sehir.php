        <h2 class="mb-5 text-center"> Şehir Hakkında</h2>
        <p>Mardin, mimari, etnografik, arkeolojik, tarihi ve görsel değerleri ile zamanın durduğu izlenimini veren Güneydoğunun şiirsel kentlerinden biridir. 
            Mardin'de, farklı dini inanışlar paralelinde, sanatsal açıdan da tarihi değeri olan camiler, türbeler, kiliseler, manastır ve benzeri dini eserler barındırmaktadır.
            Mardin ilinin yüzölçümü: 8.891 km² nüfusu ise  861.364'tür. Mardin'in trafik numarası 47'dir.</p>
