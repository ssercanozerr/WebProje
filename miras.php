        <h2 class="mb-5 text-center">Ulu Camii</h2>
        <p>Artuklu Dönemi mimari örneklerinden, dilimli kubbesi ve minaresiyle Mardin’in sembolü olan Mardin Ulu Cami kayıtlara göre iki minareli inşa edilmiştir. 
        Caminin bugün mevcut olan tek minaresinin kare kaidesindeki yazıt, yapım tarihini 1176 olarak vermektedir, fakat bugünkü minare 1888/1889 yıllarında yeni ve elektik bir üslupla yapılmıştır. 
        Bazı Süryani yazarlar kiliseden çevrildiğini söylerler. Yapı kiliseden çevrilmemiş olsa bile, yerinde eski bir kilisenin bulunması muhtemeldir.</p> 
          
        <p>Yapı 12'nci yüzyıl Artuklu Dönemi mimarisinin temel özelliklerini yansıtır. 
        Erken dönemde özellikle güneydoğuda meydana çıkan, mihrap önü kubbeli enine gelişen cami plan ve formunun çok önemli bir örneğidir. 
        Yapının malzemesi düzgün kesme taştır. Ulu Cami’nin kubbesi dıştan yivleme tekniğiyle yapılmıştır. 
        İlk olarak bu binada kullanılmış ve sonraları Mardin’de gelenek halini almıştır ki bazı geç dönem Artuklu yapılarında karakteristiktir. 
        Caminin dikdörtgen avlusu kuzeyinde kalır. Avlunun güneyinde mihrap duvarına paralel, beşik tonozlu üç neften oluşan, mihrap duvarına yakın iki nefin kubbe ile kesildiği, enine gelişmiş, mihrap önü kubbeli bir şema görülür. 
        Bu şema, aynı zamanda, çevredeki birçok yapı tarafından taklit edilmiş bir modeldir.</p>
